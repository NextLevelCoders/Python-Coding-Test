#정수형 산술 연산
a = 2
b = 3
print(b/a)  #소수점 포함    1.5
print(b//a) #몫 1
print(b%a)  #나머지 1
print(a**b) #a의 b승    8

#정수형 비트 연산
print(a&b)  #10 11  ->10    2
print(a|b)  #->11   3
print(a^b)  #xor(exclusive or,1개만 참일 경우 참)->01   1
print(~a)   #보수
print(a << 2)   #2=0010 ->1000
print(a >> 1)   #

#정수형 논리 연산
print(a and b)
print(a or b)
print(not a)
